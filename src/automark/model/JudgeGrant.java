package automark.model;

public class JudgeGrant {
	private int jgid;
	private int uid;
	private int eid;
	public int getJgid() {
		return jgid;
	}
	public void setJgid(int jgid) {
		this.jgid = jgid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
}
